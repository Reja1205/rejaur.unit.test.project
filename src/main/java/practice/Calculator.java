package practice;

public class Calculator {
    public double addition(double digit1, double digit2) {
        return digit1 + digit2;
    }

    public double subtraction(double digit1, double digit2) {
        return digit1 - digit2;
    }

    public double multiplication(double digit1, double digit2) {
        return digit1 * digit2;
    }

    public double division(double digit1, double digit2) {
        return digit1 - digit2;
    }

}
